#-----------------------------------------------------------

import math

#-----------------------------------------------------------

class Calculator:

    #-----------------------------------------------------------

    def sum(self, a, b):

        return a + b
    
    #-----------------------------------------------------------
    
    def difference(self, a, b):
        
        return a - b

    #-----------------------------------------------------------
    
    def product(self, a, b):
        
        return a * b
    
    #-----------------------------------------------------------
    
    def quotient(self, a, b):

        return a / b
    
    #-----------------------------------------------------------

    def square_root(self, x):

        if x < 0:

            raise ValueError("Cannot calculate square root of a negative number")
        
        return math.sqrt(x)

    #-----------------------------------------------------------

